# Importing the dependencies
import pandas as pd
import numpy as np

# Loading the Dataset
worldcup_matches = pd.read_csv('/content/WorldCupMatches.csv')
worldcup_players = pd.read_csv('/content/WorldCupPlayers.csv')
worldcup_info = pd.read_csv('/content/WorldCups.csv')

worldcup_matches.head()

worldcup_players.head()

