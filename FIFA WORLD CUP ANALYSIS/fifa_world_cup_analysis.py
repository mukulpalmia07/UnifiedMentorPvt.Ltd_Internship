# Data Integration: Merging Dataset
merged_data = pd.merge(worldcup_matches, worldcup_players, on = ['RoundID', 'MatchID'])
merged_data = pd.merge(merged_data, worldcup_info, on = 'Year')

# Checking the info the merged dataset
merged_data.info()

# Checking missing value
merged_data.isnull().sum()

# Dropping irrelevant columns and null values
merged_data.dropna(inplace = True)

